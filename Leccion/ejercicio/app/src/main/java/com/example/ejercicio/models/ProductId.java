package com.example.ejercicio.models;

public class ProductId {
    private int id;

    // Constructor vacío
    public ProductId() {}

    // Constructor con parámetro
    public ProductId(int id) {
        this.id = id;
    }

    // Getter y setter para 'id'
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}

