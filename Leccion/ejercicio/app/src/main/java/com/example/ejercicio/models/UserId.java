package com.example.ejercicio.models;

public class UserId {
    private int id;

    // Constructor vacío
    public UserId() {}

    // Constructor con parámetro
    public UserId(int id) {
        this.id = id;
    }

    // Getter y setter para 'id'
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
